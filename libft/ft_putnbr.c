#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		if (n / 10)
			ft_putnbr(-(n / 10));
		n = -(n % 10) + '0';
		write(1, &n, 1);
	}
	else
	{
		if (n / 10)
			ft_putnbr(n / 10);
		n = n % 10 + '0';
		write(1, &n, 1);
	}
}
